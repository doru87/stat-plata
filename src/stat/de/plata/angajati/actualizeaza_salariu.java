    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        String sql ="select * from angajati where id=?";
        
        try {
            ps = conexiune.prepareStatement(sql);
            ps.setString(1,jTextField1.getText());
            rs = ps.executeQuery();
            
            String nume_angajat = rs.getString("nume");
            jTextField2.setText(nume_angajat);
            String prenume_angajat = rs.getString("prenume");
            jTextField3.setText(prenume_angajat);
            String salariu_angajat = rs.getString("salariu");
            jTextField4.setText(salariu_angajat);
            String departament_angajat = rs.getString("departament");
            jTextField5.setText(departament_angajat);
            
        } catch (SQLException ex) {
            Logger.getLogger(actualizeaza_salariu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTextField1KeyReleased

