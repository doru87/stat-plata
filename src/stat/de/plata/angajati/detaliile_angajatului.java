    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        
        JFileChooser file = new JFileChooser();
          file.setCurrentDirectory(new File(System.getProperty("user.home")));
          //filter the files
          FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg","gif","png");
          file.addChoosableFileFilter(filter);
          int result = file.showSaveDialog(null);
           //if the user click on save in Jfilechooser
          if(result == JFileChooser.APPROVE_OPTION){
              File selectedFile = file.getSelectedFile();
              path = selectedFile.getAbsolutePath();
              ImageIcon image = new ImageIcon(new ImageIcon(path).getImage().getScaledInstance(imagine.getWidth(), imagine.getHeight(), Image.SCALE_DEFAULT));
              imagine.setIcon(image);
    }//GEN-LAST:event_jMenuItem2ActionPerformed
    
    }
    
    public byte [] return_image() throws IOException{
                File fnew=new File(path);
                BufferedImage originalImage=ImageIO.read(fnew);
                ByteArrayOutputStream baos=new ByteArrayOutputStream();
                ImageIO.write(originalImage, "jpg", baos );
                imageInByte=baos.toByteArray();
                return imageInByte;
}

    
