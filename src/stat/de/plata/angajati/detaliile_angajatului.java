            ps.setString(7, jTextField8.getText());
            ps.setString(8, sex);
            ps.setString(9, jTextField9.getText());
            ps.setString(10, jTextField11.getText());
            ps.setString(11, jTextField10.getText());
            ps.setBytes(12, return_image());
            ps.executeUpdate();
            
   
            
            
